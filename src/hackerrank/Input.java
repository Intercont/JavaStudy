package hackerrank;

public class Input {

    private Boolean flexForced;
    private String regime;
    private String firstFactor;
    private String secondFactor;

    public Boolean getFlexForced() {
        return flexForced;
    }

    public void setFlexForced(Boolean flexForced) {
        this.flexForced = flexForced;
    }

    public String getRegime() {
        return regime;
    }

    public void setRegime(String regime) {
        this.regime = regime;
    }

    public String getFirstFactor() {
        return firstFactor;
    }

    public void setFirstFactor(String firstFactor) {
        this.firstFactor = firstFactor;
    }

    public String getSecondFactor() {
        return secondFactor;
    }

    public void setSecondFactor(String secondFactor) {
        this.secondFactor = secondFactor;
    }
}
